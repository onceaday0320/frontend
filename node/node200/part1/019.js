/**
 * JSON이란, JavaScript Object Notation의 약자이다. 번역하면 '자바스크립트 오브젝트 표기법'이다.
 * 프로그래밍에서 오브젝트는 현실의 '어떤 것'이라고 할 수 있다. 이것, 저것, 누구, 무엇등 음식, 사람, 옷 집 모든 것들을 '것'으로 포현할 수 있다.
 */
const user = {};
user.name = 'kyeongrok';
user.age = 31;

console.log(user);

const user2 = {name: 'kyeongrok', age: 31};
