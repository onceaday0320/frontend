/**
 * 비교 연산자는 두 개의 값을 비교하여 true와 false 결과값을 반환한다. 비교 연산자의 종류에는 값이 동등한지 비교하는
 일치 연산자와 값의 관계를 비교하는 관계 연산자가 있다.
 */

console.log(5 == 5);
console.log('5' == 5);
console.log(5 == '5');
console.log(5 != 1);
console.log(5 != '1');
console.log(5 != '5');
console.log(5 === 5);
console.log(5 === '5');
console.log(5 !== 10);
console.log(5 !== '5');
console.log('-------');
console.log(5 > 3);
console.log(5 < 3);
console.log(5 <= 6);
console.log(5 >= 5);
console.log('-------');
console.log('Italy' > 'America');
console.log('Korea' < 'korea');